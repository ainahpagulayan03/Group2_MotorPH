    private void gross_inc(){
        
        int salary = Integer.parseInt(monthly_rate_payroll.getText());
        int overtime = Integer.parseInt(overtime_payroll.getText());
        double hourlyrate = (Double.parseDouble(monthly_rate_payroll.getText())/22)/8;
        double overtimepay = overtime*hourlyrate;
        
        
        gross_inc_payroll.setText(Double.toString(salary+overtimepay));
        gross_inc_payroll_sum.setText(Double.toString(salary+overtimepay));
        
        
    }
    
    
    private void tax_calculation(){
        
        int salary = Integer.parseInt(monthly_rate_payroll.getText());
        
        double gross = Double.parseDouble(gross_inc_payroll.getText());
        double phil = Double.parseDouble(phil_payroll.getText());
        double sss = Double.parseDouble(sss_payroll.getText());
        double hdmf = Double.parseDouble(pag_ibig_payroll.getText());
        
        double sal = gross-phil-sss-hdmf;
        
        
        
        double first = 0.20*(sal-20833);
        double second = 0.25*(sal-33333);
        double third = 0.30*(sal-66667);
        double forth = 0.32*(sal-166667);
        double fifth = 0.35*(sal-666667);
        
        int se = 2500;
        int th = 10833;
        double fo = 40833.33;
        double fi = 200833.33;

        
        if(salary<20832){
            
            tax_payroll.setText("0");
            
        }else if(salary<33333){
            
            tax_payroll.setText(Double.toString(first));
            
        }else if(salary<66667){
            
            tax_payroll.setText(Double.toString(second+se));
        
        }else if(salary<166667){
            
            tax_payroll.setText(Double.toString(third+th));
            
        }else if(salary<666667){
            
            tax_payroll.setText(Double.toString(forth+fo));
            
        }else{
            tax_payroll.setText(Double.toString(fifth+fi));
        }
         
    }
    
    
